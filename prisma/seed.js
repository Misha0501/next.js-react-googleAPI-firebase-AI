// Exterior counters — minimise reuse across listings
let aptExtIdx = 0,
  hseExtIdx = 0,
  landIdx = 0,
  parkingIdx = 0;

function exterior(sub) {
  // villas use house-exteriors (villa-exteriors pool not used in seed)
  if (sub === "house" || sub === "villa")
    return IMG.hseExt[hseExtIdx++ % IMG.hseExt.length];
  return IMG.aptExt[aptExtIdx++ % IMG.aptExt.length];
}

// Image count: 10%→1, 15%→2, 25%→3, 50%→4  (based on listingIdx 0-149)
function imageCount(idx) {
  if (idx < 15) return 1;
  if (idx < 38) return 2;
  if (idx < 75) return 3;
  return 4;
}

function buildImages(sub, idx, hasBalcony) {
  // Land and parking: always exactly 1 unique image from dedicated pools
  if (sub === "land") {
    const img = IMG.land[landIdx++ % IMG.land.length];
    return [{ ...img, positionInListing: 0 }];
  }
  if (sub === "parking") {
    const img = IMG.parking[parkingIdx++ % IMG.parking.length];
    return [{ ...img, positionInListing: 0 }];
  }

  const ext = exterior(sub);
  const count = imageCount(idx);
  const imgs = [{ ...ext, positionInListing: 0 }];
  if (count === 1) return imgs;

  const lr = IMG.living[idx % IMG.living.length];
  imgs.push({ ...lr, positionInListing: 1 });
  if (count === 2) return imgs;

  const slot2 =
    idx % 2 === 0
      ? IMG.kitchen[idx % IMG.kitchen.length]
      : IMG.bedroom[idx % IMG.bedroom.length];
  imgs.push({ ...slot2, positionInListing: 2 });
  if (count === 3) return imgs;

  // Slot 3: balcony if the property has one, otherwise vary by type
  let slot3;
  if (hasBalcony) {
    slot3 = IMG.balcony[idx % IMG.balcony.length];
  } else {
    const r = idx % 3;
    if (r === 0) slot3 = IMG.bathroom[idx % IMG.bathroom.length];
    else if (r === 1) slot3 = IMG.bedroom[(idx + 5) % IMG.bedroom.length];
    else slot3 = IMG.kitchen[(idx + 5) % IMG.kitchen.length];
  }
  imgs.push({ ...slot3, positionInListing: 3 });
  return imgs;
}

// ── Price history ─────────────────────────────────────────────────────────────

function euros(v) {
  // Rounding unit scales with price so small prices still get a visible change
  const unit =
    v >= 100000
      ? 1000
      : v >= 20000
        ? 500
        : v >= 5000
          ? 100
          : v >= 1000
            ? 50
            : 10;
  return Math.round(v / unit) * unit;
}

function chronological(history) {
  return [...history].sort(
    (a, b) => a.createdAt.getTime() - b.createdAt.getTime(),
  );
}

function buildPriceHistory(price, idx, listedAt) {
  // listedAt is the listing's createdAt — the first entry must use this date so the
  // app labels it "Listed". Every listing gets at least 2 entries.
  // Anchor days are computed proportionally so dates are always strictly increasing
  // regardless of how long the listing has been live.
  const now = Date.now();
  const msListed = now - listedAt.getTime();
  const daysListed = Math.floor(msListed / MS_PER_DAY);

  function after(d) {
    return new Date(listedAt.getTime() + d * MS_PER_DAY);
  }
  function afterMs(ms) {
    return new Date(listedAt.getTime() + ms);
  }

  // <5 days: change placed at midpoint of elapsed time
  if (daysListed < 5) {
    const p0 = euros(price * (1.03 + (idx % 5) * 0.01));
    return chronological([
      { currency: "EUR", price: p0, tax: null, createdAt: listedAt },
      {
        currency: "EUR",
        price: price,
        tax: null,
        createdAt: afterMs(Math.floor(msListed * 0.5)),
      },
    ]);
  }

  // Proportional anchors — always strictly increasing, always before "now"
  const w = daysListed - 1; // available days (1-day buffer before now)
  const d1 = Math.max(1, Math.floor(w * 0.3));
  const d2 = Math.max(d1 + 3, Math.floor(w * 0.62));
  const d3 = Math.max(d2 + 3, Math.floor(w * 0.84));

  // <20 days: 2 entries only (not enough room for meaningful multi-step history)
  if (daysListed < 20) {
    const p0 = euros(price * (1.05 + (idx % 8) * 0.01));
    return chronological([
      { currency: "EUR", price: p0, tax: null, createdAt: listedAt },
      { currency: "EUR", price: price, tax: null, createdAt: after(d1) },
    ]);
  }

  const scenario = idx % 10;

  if (scenario <= 6) {
    // 70%: listed high → mid reduction → current
    const p0 = euros(price * (1.08 + (idx % 10) * 0.01));
    const p1 = euros(price * (1.03 + (idx % 5) * 0.01));
    return chronological([
      { currency: "EUR", price: p0, tax: null, createdAt: listedAt },
      { currency: "EUR", price: p1, tax: null, createdAt: after(d1) },
      { currency: "EUR", price: price, tax: null, createdAt: after(d2) },
    ]);
  }

  if (scenario <= 8) {
    // 20%: 4 entries with 3 step-down reductions (only when listing is old enough)
    if (daysListed >= 40) {
      const p0 = euros(price * (1.15 + (idx % 8) * 0.01));
      const p1 = euros(price * (1.08 + (idx % 6) * 0.01));
      const p2 = euros(price * (1.03 + (idx % 4) * 0.01));
      return chronological([
        { currency: "EUR", price: p0, tax: null, createdAt: listedAt },
        { currency: "EUR", price: p1, tax: null, createdAt: after(d1) },
        { currency: "EUR", price: p2, tax: null, createdAt: after(d2) },
        { currency: "EUR", price: price, tax: null, createdAt: after(d3) },
      ]);
    }
    // Fall back to 3 entries for younger listings
    const p0 = euros(price * (1.08 + (idx % 10) * 0.01));
    const p1 = euros(price * (1.03 + (idx % 5) * 0.01));
    return chronological([
      { currency: "EUR", price: p0, tax: null, createdAt: listedAt },
      { currency: "EUR", price: p1, tax: null, createdAt: after(d1) },
      { currency: "EUR", price: price, tax: null, createdAt: after(d2) },
    ]);
  }

  // 10%: listed slightly lower → small increase → current (higher)
  const p0 = euros(price * (0.94 + (idx % 5) * 0.01));
  const p1 = euros(price * (0.97 + (idx % 4) * 0.01));
  return chronological([
    { currency: "EUR", price: p0, tax: null, createdAt: listedAt },
    { currency: "EUR", price: p1, tax: null, createdAt: after(d1) },
    { currency: "EUR", price: price, tax: null, createdAt: after(d2) },
  ]);
}

