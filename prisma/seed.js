function buildDescription(p) {
  if (p.sub === "land") {
    const use = p.zone ? p.zone : "residential or investment";
    return (
      `Building plot for sale in ${p.nbhd}, ${p.city}. ` +
      `The land spans ${p.area} sqm and is suitable for ${use} development. ` +
      `Fully regulated with all utilities available nearby. ` +
      `Prime location in ${p.nbhd} with excellent transport links and infrastructure.`
    );
  }
  if (p.sub === "parking") {
    return (
      `Parking space for sale in ${p.nbhd}, ${p.city}. ` +
      `Secure ${p.covered ? "covered " : ""}parking spot of ${p.area} sqm, ` +
      `located in a ${p.indoor ? "underground garage" : "surface car park"} with 24/7 access. ` +
      `Ideal for residents and investors in ${p.nbhd}.`
    );
  }
  const type =
    p.sub === "villa" ? "villa" : p.sub === "house" ? "house" : "apartment";
  const action = p.lt === "RENT" ? "rent" : "sale";
  const balcony = p.aOut ? ` with a ${p.aOut} sqm balcony` : "";
  const garden = p.aLand ? ` set on a ${p.aLand} sqm private plot` : "";
  const bedLabel = p.bed === 0 ? "Studio" : `${p.bed}-bedroom`;
  const floorLine = p.fl ? `, floor ${p.fl} of ${p.tfl}` : "";
  const furnished =
    p.int === "FURNISHED"
      ? "fully furnished and move-in ready"
      : "unfurnished — ideal for buyers who prefer their own interior";
  const condition = {
    EXCELLENT: "in excellent condition with modern finishes",
    GOOD: "in good overall condition",
    FAIR: "in fair condition with some cosmetic updates needed",
  }[p.upk];
  return (
    `${bedLabel} ${type} for ${action} in ${p.nbhd}, ${p.city}${balcony}${garden}. ` +
    `The property spans ${p.area} sqm${p.aLiv ? ` (${p.aLiv} sqm living area)` : ""}${floorLine}, ` +
    `${furnished} and ${condition}. ` +
    `Conveniently located in ${p.nbhd} with easy access to public transport, shops, and green spaces.`
  );
}

function buildKeyPoints(p) {
  if (p.sub === "land") {
    return [
      ["Land area", `${p.area} sqm of regulated land ready for development.`],
      [
        "Prime location",
        `Located in ${p.nbhd}, ${p.city} — well connected and in demand.`,
      ],
      [
        "Utilities",
        "Water, electricity and road access available at the plot boundary.",
      ],
    ];
  }
  if (p.sub === "parking") {
    return [
      [
        "Secure parking",
        `${p.covered ? "Covered" : "Surface"} parking space with ${p.indoor ? "underground garage" : "barrier"} access.`,
      ],
      [
        "Prime location",
        `Located in ${p.nbhd}, ${p.city} — high-demand area for parking.`,
      ],
      [
        "Low maintenance",
        "No maintenance fees beyond standard building costs.",
      ],
    ];
  }
  const kp = [];
  if (p.aOut)
    kp.push([
      "Outdoor space",
      `${p.aOut} sqm balcony or terrace with natural light.`,
    ]);
  if (p.park)
    kp.push([
      "Parking included",
      `${p.park} dedicated parking space${p.park > 1 ? "s" : ""} included.`,
    ]);
  kp.push([
    "Prime location",
    `Located in ${p.nbhd}, ${p.city} — close to transport, shops and everyday services.`,
  ]);
  if (p.heat === "CENTRAL")
    kp.push([
      "Central heating",
      "Building-wide central heating with individual metering.",
    ]);
  else
    kp.push([
      "Independent heating",
      "Gas boiler providing full control over heating costs.",
    ]);
  if (p.built >= 2015)
    kp.push([
      "Modern build",
      `Constructed in ${p.built} with contemporary materials and finishes.`,
    ]);
  if (p.aLand)
    kp.push([
      "Private garden",
      `${p.aLand} sqm of private outdoor land — ideal for outdoor living.`,
    ]);
  return kp.slice(0, 3);
}

